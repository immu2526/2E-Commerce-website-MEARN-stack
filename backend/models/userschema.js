const mongoose = require("mongoose");
// let Schema = mongoose.Schema();

const userSchema = new mongoose.Schema({
  userName: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
    default: "user",
  },
});

const User = mongoose.model("User", userSchema);

module.exports = User;
